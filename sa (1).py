# ROTA VE KISIT COZUMLEME
# Round-Robin ve Bolunmus Teslimat

def rotalari_hesapla(dizi):
    # arac filosunun calisma yapisini takip eden sozluk  yapilari.
    arac_rotalari = {v: {1: [], 2: []} for v in range(1, 9)}
    arac_toplanan_cop = {v: {1: [], 2: []} for v in range(1, 9)}
    arac_calisma_sureleri = {v: 0 for v in range(1, 9)}
    arac_sefer_yukleri = {v: {1: 0, 2: 0} for v in range(1, 9)}
    arac_aktif_sefer = {v: 1 for v in range(1, 9)}
    arac_son_durum = {v: 0 for v in range(1, 9)}

    ziyaret_edilen_dugumler = set()
    anlik_talepler = list(talepler)
    toplam_mesafe = 0
    ceza_puani = 0

    suanki_arac = 1
    dugum_indeksi = 0

    # Permutasyon dizisi uzerinden atik toplama kismi
    while dugum_indeksi < len(dizi):
        hedef_dugum = dizi[dugum_indeksi]
        istenen_cop = anlik_talepler[hedef_dugum]

        # Sitenin atigi sifirlandiysa bir sonraki dugume geciren kisim
        if istenen_cop == 0:
            dugum_indeksi += 1
            continue

        aktif_sefer = arac_aktif_sefer[suanki_arac]

        # Aracin izin verilen sefer sayisi asildiysa guncel arac sirasi degistiren kisim
        if aktif_sefer > 2:
            suanki_arac = (suanki_arac % 8) + 1
            # 8 aracin tum seferleri dolduysa islemi durduran kisim
            if all(arac_aktif_sefer[v] > 2 for v in range(1, 9)):
                break
            continue

        mevcut_yuk = arac_sefer_yukleri[suanki_arac][aktif_sefer]
        alinabilecek_kapasite = arac_kapasitesi - mevcut_yuk

        # Arac doluysa depoya geri dondurulur, yeni sefer acilir
        if alinabilecek_kapasite == 0:
            son_gidilen_yer = arac_son_durum[suanki_arac]
            if son_gidilen_yer != 0:
                arac_calisma_sureleri[suanki_arac] += sure_matrisi[son_gidilen_yer][0]
                toplam_mesafe += mesafe_matrisi[son_gidilen_yer][0]
            arac_aktif_sefer[suanki_arac] += 1
            arac_son_durum[suanki_arac] = 0
            continue

        # Bolunmus Teslimat kismi, Arac dugumdeki copun tamamini alamasa bile alabilecegi kadarini alir
        alinacak_cop = min(istenen_cop, alinabilecek_kapasite)
        son_gidilen_yer = arac_son_durum[suanki_arac]

        gidis_suresi = sure_matrisi[son_gidilen_yer][hedef_dugum]
        donus_suresi = sure_matrisi[hedef_dugum][0]

        # Yasal mesai siniri 480 dk kontrolu
        if arac_calisma_sureleri[suanki_arac] + gidis_suresi + bosaltma_suresi + donus_suresi <= gunluk_mesai_siniri:

            # Dugum rotaya eklenir ve toplanan miktar kaydedilir
            if hedef_dugum not in arac_rotalari[suanki_arac][aktif_sefer]:
                arac_rotalari[suanki_arac][aktif_sefer].append(hedef_dugum)
                arac_toplanan_cop[suanki_arac][aktif_sefer].append(alinacak_cop)
            else:
                idx = arac_rotalari[suanki_arac][aktif_sefer].index(hedef_dugum)
                arac_toplanan_cop[suanki_arac][aktif_sefer][idx] += alinacak_cop

            ziyaret_edilen_dugumler.add(hedef_dugum)

            arac_sefer_yukleri[suanki_arac][aktif_sefer] += alinacak_cop
            anlik_talepler[hedef_dugum] -= alinacak_cop
            arac_calisma_sureleri[suanki_arac] += gidis_suresi + bosaltma_suresi
            toplam_mesafe += mesafe_matrisi[son_gidilen_yer][hedef_dugum]
            arac_son_durum[suanki_arac] = hedef_dugum

            # Cop bittiyse sonraki siteye gecilir.
            if anlik_talepler[hedef_dugum] == 0:
                dugum_indeksi += 1

            # Round-Robin, Is yukunun siradaki araca aktarimi, is yukunu dengeliyo
            suanki_arac = (suanki_arac % 8) + 1

        else:
            # Mesai dolduysa arac depoya dondurulur.
            if son_gidilen_yer != 0:
                arac_calisma_sureleri[suanki_arac] += sure_matrisi[son_gidilen_yer][0]
                toplam_mesafe += mesafe_matrisi[son_gidilen_yer][0]
            arac_aktif_sefer[suanki_arac] += 1
            arac_son_durum[suanki_arac] = 0

            if all(arac_aktif_sefer[v] > 2 for v in range(1, 9)):
                break

    # Gun sonu depoya donus islemleri saglanir
    for v in range(1, 9):
        son_gidilen_yer = arac_son_durum[v]
        if son_gidilen_yer != 0:
            arac_calisma_sureleri[v] += sure_matrisi[son_gidilen_yer][0]
            toplam_mesafe += mesafe_matrisi[son_gidilen_yer][0]

    # Toplanamayan atik icin ceza
    for n in range(1, 37):
        if anlik_talepler[n] > 0:
            ceza_puani += anlik_talepler[n] * 1000

    return toplam_mesafe + ceza_puani, arac_rotalari, arac_calisma_sureleri, sum(anlik_talepler), arac_toplanan_cop, ziyaret_edilen_dugumler

# OPTIMIZASYON , SIMULATED ANNEALING
def tavlama_benzetimi_calistir(maksimum_iterasyon=30000):
    # Baslangic cozumunun uretilmesi.
    suanki_cozum = list(range(1, 37))
    random.seed(42)
    random.shuffle(suanki_cozum)

    suanki_maliyet, _, _, _, _, _ = rotalari_hesapla(suanki_cozum)
    en_iyi_cozum = list(suanki_cozum)
    en_iyi_maliyet = suanki_maliyet

    sicaklik = 1200.0
    soguma_orani = 0.9995

    for i in range(maksimum_iterasyon):
        yeni_cozum = list(suanki_cozum)

        # Takas (Swap) kismi, Rastgele iki dugumun yerini degistirir.
        idx1, idx2 = random.sample(range(36), 2)
        yeni_cozum[idx1], yeni_cozum[idx2] = yeni_cozum[idx2], yeni_cozum[idx1]

        maliyet, _, _, _, _, _ = rotalari_hesapla(yeni_cozum)

        # Yeni maliyet dusukse kabul edilir.
        if maliyet < suanki_maliyet:
            suanki_cozum = yeni_cozum
            suanki_maliyet = maliyet
            if maliyet < en_iyi_maliyet:
                en_iyi_cozum = yeni_cozum
                en_iyi_maliyet = maliyet
        else:
            # Metropolis kriteri, Kotu cozumlere belirli bir olasilikla izin verilir.
            fark = maliyet - suanki_maliyet
            if random.random() < math.exp(-fark / sicaklik):
                suanki_cozum = yeni_cozum
                suanki_maliyet = maliyet

        sicaklik *= soguma_orani

    return en_iyi_cozum


